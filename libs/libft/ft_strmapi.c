#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*p;
	unsigned int	i;
	unsigned int	size;

	i = 0;
	if (!s)
		return (NULL);
	size = ft_strlen(s);
	p = (char *) malloc ((size + 1) * sizeof(char));
	if (!p)
		return (NULL);
	while (s[i] != '\0')
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return ((char *) p);
}
